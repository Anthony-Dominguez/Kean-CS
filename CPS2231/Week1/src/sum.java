public class sum {
    public static void main(String[] args) {

        int total=4;
        int i=2;
        while(i<5){
            total=total*i;
            i=i+1;

        }
        System.out.println("The total is "+ total);

        int total1=4;

        for (int j = 2; j < 5 ; j++) {
            total1=total1*j;

        }
        System.out.println(total1);

    }

}
